places_to_visit = [
    "Kyoto, Japan ",
    "The Swiss Alps, Switzerland",
    "Machu Picchu, Peru",
    "Great Barrier Reef, Australia",
    "Cappadocia, Turkey"
]
print(f"original order = {places_to_visit}")
sorted_places_to_visit = sorted(places_to_visit)
print( f"sorted order = {sorted_places_to_visit}")
print( f"original order = {places_to_visit}")
reversed_sorted_places_to_visit = sorted(places_to_visit,reverse=True)
print(f"reversed sorted order = {reversed_sorted_places_to_visit}")
print( f"original order = {places_to_visit}")
places_to_visit.reverse()
print( f"reversed order = {places_to_visit}")
places_to_visit.reverse()
print(f"original order = {places_to_visit}")